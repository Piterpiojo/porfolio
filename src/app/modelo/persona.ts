export class Persona{
nombre:string;
Apellido:string;
foto:string;
descripcion:string;
banner:string;
titulo:string;


constructor(nombre:string,
  Apellido:string,
  foto:string,
  descripcion:string,
  banner:string,
  titulo:string){

  this.nombre = nombre;
  this.Apellido = Apellido;
  this.foto = foto;
  this.descripcion = descripcion;
  this.banner = banner;
  this.titulo = titulo;
}
}
